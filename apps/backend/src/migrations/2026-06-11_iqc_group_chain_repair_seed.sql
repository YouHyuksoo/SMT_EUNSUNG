-- IQC 품목→검사항목 체인 복구 시드
-- 배경(2026-06-11 실측): IQC_PART_LINKS가 존재하지 않는 그룹(GRP-*)을 참조하고,
--   실재 그룹(IQC_GROUPS의 IGR-*)에는 IQC_GROUP_ITEMS 매핑이 0건이라
--   품목→그룹→그룹항목→검사항목풀(IQC_ITEM_POOL) 체인이 어디서도 성립하지 않았다.
--   (IQC 검사결과 등록 모달에 검사항목이 일부/누락 표시되는 원인)
-- 처리:
--   1) 깨진 GRP-* 링크를 의미에 맞는 실재 IGR-* 그룹으로 UPDATE
--   2) IGR-* 그룹별 풀 검사항목 매핑 시드 (WHERE NOT EXISTS, 재실행 안전)
-- 주의: 그룹별 항목 구성은 풀 항목 의미 기반 예시 — 실 운영 기준은 IQC 검사그룹 화면에서 조정.
-- 사이트: JSHANES(40/1000).

-- 1) 깨진 GRP-* 링크 → 실재 IGR-* 그룹으로 정정 (대상 GRP-*가 IQC_GROUPS에 없을 때만)
UPDATE IQC_PART_LINKS l
   SET l.GROUP_ID = CASE l.GROUP_ID
                      WHEN 'GRP-CBL'  THEN 'IGR-WIRE'
                      WHEN 'GRP-TMN'  THEN 'IGR-TERM'
                      WHEN 'GRP-CNTR' THEN 'IGR-CONN'
                      WHEN 'GRP-HLD'  THEN 'IGR-CONN'
                      WHEN 'GRP-RSL'  THEN 'IGR-CONN-WP'
                    END,
       l.UPDATED_BY = 'seed', l.UPDATED_AT = SYSTIMESTAMP
 WHERE l.COMPANY = '40' AND l.PLANT_CD = '1000'
   AND l.GROUP_ID IN ('GRP-CBL','GRP-TMN','GRP-CNTR','GRP-HLD','GRP-RSL')
   AND NOT EXISTS (
     SELECT 1 FROM IQC_GROUPS g
      WHERE g.GROUP_CODE = l.GROUP_ID AND g.COMPANY = '40' AND g.PLANT_CD = '1000'
   )
/

-- 2) IGR-* 그룹별 검사항목 매핑 시드 (실재 그룹 + 실재 풀 항목 + 미존재 매핑만)
INSERT INTO IQC_GROUP_ITEMS (GROUP_ID, INSP_ITEM_ID, SEQ, COMPANY, PLANT_CD, CREATED_BY, CREATED_AT, UPDATED_AT)
SELECT src.grp, src.item, src.seq, '40', '1000', 'seed', SYSTIMESTAMP, SYSTIMESTAMP
  FROM (
    SELECT 'IGR-WIRE' grp, 'IQC-VISUAL' item, 10 seq FROM DUAL UNION ALL
    SELECT 'IGR-WIRE', 'IQC-WIRE-OD', 20 FROM DUAL UNION ALL
    SELECT 'IGR-WIRE', 'IQC-WIRE-COLOR', 30 FROM DUAL UNION ALL
    SELECT 'IGR-WIRE', 'IQC-LENGTH', 40 FROM DUAL UNION ALL
    SELECT 'IGR-WIRE', 'IQC-PRINT', 50 FROM DUAL UNION ALL
    SELECT 'IGR-TERM', 'IQC-VISUAL', 10 FROM DUAL UNION ALL
    SELECT 'IGR-TERM', 'IQC-DIMENSION', 20 FROM DUAL UNION ALL
    SELECT 'IGR-TERM', 'IQC-CRIMP-DIM', 30 FROM DUAL UNION ALL
    SELECT 'IGR-TERM', 'IQC-PLATING', 40 FROM DUAL UNION ALL
    SELECT 'IGR-CONN', 'IQC-VISUAL', 10 FROM DUAL UNION ALL
    SELECT 'IGR-CONN', 'IQC-CONN-DIM', 20 FROM DUAL UNION ALL
    SELECT 'IGR-CONN', 'IQC-LOCK', 30 FROM DUAL UNION ALL
    SELECT 'IGR-CONN', 'IQC-FIT', 40 FROM DUAL UNION ALL
    SELECT 'IGR-CONN-WP', 'IQC-VISUAL', 10 FROM DUAL UNION ALL
    SELECT 'IGR-CONN-WP', 'IQC-CONN-DIM', 20 FROM DUAL UNION ALL
    SELECT 'IGR-CONN-WP', 'IQC-LOCK', 30 FROM DUAL UNION ALL
    SELECT 'IGR-CONN-WP', 'IQC-FIT', 40 FROM DUAL UNION ALL
    SELECT 'IGR-CONN-WP', 'IQC-HARDNESS', 50 FROM DUAL UNION ALL
    SELECT 'IGR-PROT', 'IQC-VISUAL', 10 FROM DUAL UNION ALL
    SELECT 'IGR-PROT', 'IQC-LENGTH', 20 FROM DUAL UNION ALL
    SELECT 'IGR-PROT', 'IQC-FLEX', 30 FROM DUAL UNION ALL
    SELECT 'IGR-PROT', 'IQC-ADHESION', 40 FROM DUAL
  ) src
 WHERE EXISTS (SELECT 1 FROM IQC_GROUPS g WHERE g.GROUP_CODE = src.grp AND g.COMPANY = '40' AND g.PLANT_CD = '1000')
   AND EXISTS (SELECT 1 FROM IQC_ITEM_POOL p WHERE p.INSP_ITEM_CODE = src.item AND p.COMPANY = '40' AND p.PLANT_CD = '1000')
   AND NOT EXISTS (
     SELECT 1 FROM IQC_GROUP_ITEMS gi
      WHERE gi.GROUP_ID = src.grp AND gi.INSP_ITEM_ID = src.item
        AND gi.COMPANY = '40' AND gi.PLANT_CD = '1000'
   )
/

