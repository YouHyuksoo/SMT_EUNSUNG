-- IQC006 입하실적조회: 상태/구분 공통코드 + 메뉴 등록 (멱등 MERGE)
-- 대상: JSHANES company=40 plant=1000

-- ── 도출 상태 배지 (ARRIVAL_RESULT_STATUS) ──────────────────────────
MERGE INTO COM_CODES t
USING (SELECT 'ARRIVAL_RESULT_STATUS' GROUP_CODE,'ARRIVED' DETAIL_CODE,'입하완료' CODE_NAME,1 SORT_ORDER,'bg-slate-600 text-white' ATTR1,'40' COMPANY,'1000' PLANT_CD FROM DUAL) s
ON (t.GROUP_CODE=s.GROUP_CODE AND t.DETAIL_CODE=s.DETAIL_CODE AND t.COMPANY=s.COMPANY AND t.PLANT_CD=s.PLANT_CD)
WHEN MATCHED THEN UPDATE SET t.CODE_NAME=s.CODE_NAME, t.SORT_ORDER=s.SORT_ORDER, t.ATTR1=s.ATTR1, t.USE_YN='Y'
WHEN NOT MATCHED THEN INSERT (GROUP_CODE,DETAIL_CODE,CODE_NAME,SORT_ORDER,USE_YN,ATTR1,COMPANY,PLANT_CD,CREATED_BY) VALUES (s.GROUP_CODE,s.DETAIL_CODE,s.CODE_NAME,s.SORT_ORDER,'Y',s.ATTR1,s.COMPANY,s.PLANT_CD,'claude');
/
