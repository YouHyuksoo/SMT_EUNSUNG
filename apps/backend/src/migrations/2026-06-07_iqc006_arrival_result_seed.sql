USING (SELECT '40' COMPANY,'1000' PLANT_CD,'MAT_ARRIVAL_RESULT' MENU_CODE,'MATERIAL' CATEGORY_CODE,35 SORT_ORDER FROM DUAL) s
ON (t.COMPANY=s.COMPANY AND t.PLANT_CD=s.PLANT_CD AND t.MENU_CODE=s.MENU_CODE)
