DECLARE
  v_count NUMBER;
BEGIN
  -- PROD_RESULTS production type for trial/mass production split.
  -- Existing rows default to MASS. New rows are decided by backend first-inspection state.
  SELECT COUNT(*)
    INTO v_count
    FROM USER_TAB_COLUMNS
   WHERE TABLE_NAME = 'PROD_RESULTS'
     AND COLUMN_NAME = 'PRODUCTION_TYPE';

  IF v_count = 0 THEN
    EXECUTE IMMEDIATE 'ALTER TABLE PROD_RESULTS ADD (PRODUCTION_TYPE VARCHAR2(20) DEFAULT ''MASS'' NOT NULL)';
  END IF;
END;
/

UPDATE PROD_RESULTS
   SET PRODUCTION_TYPE = 'MASS'
 WHERE PRODUCTION_TYPE IS NULL
/

DECLARE
  v_count NUMBER;
BEGIN
  SELECT COUNT(*)
    INTO v_count
    FROM USER_CONSTRAINTS
   WHERE TABLE_NAME = 'PROD_RESULTS'
     AND CONSTRAINT_NAME = 'CK_PROD_RESULTS_PROD_TYPE';

  IF v_count = 0 THEN
    EXECUTE IMMEDIATE 'ALTER TABLE PROD_RESULTS ADD CONSTRAINT CK_PROD_RESULTS_PROD_TYPE CHECK (PRODUCTION_TYPE IN (''TRIAL'', ''MASS''))';
  END IF;
END;
/

COMMENT ON COLUMN PROD_RESULTS.PRODUCTION_TYPE IS '생산유형: TRIAL=시생산, MASS=양산'
/
