-- 검사의뢰(의뢰검사, DELEGATE) 테스트 데이터
-- /quality/request-inspect(의뢰검사 입력)는 SELF_INSPECT_RESULTS에서
-- INSPECT_METHOD='DELEGATE' AND STATUS='PENDING' 행을 의뢰검사 대기 목록으로 보여준다.
-- 실제 DELEGATE 자주검사 항목(SELF_INSPECT_ITEMS)과 실제 작업지시(JOB_ORDERS)를 참조해 5건 생성.
-- 테스트 데이터 식별/정리용으로 REMARK에 '[SEED] 검사의뢰 테스트' 마킹.
-- ID는 TypeORM uuid(대시 포함 소문자)와 동일 포맷으로 생성.
-- 재실행 안전: 동일 (작업지시+검사항목+시점)의 PENDING DELEGATE가 있으면 건너뜀.

