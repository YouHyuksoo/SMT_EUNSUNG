  (ID, ORDER_NO, PROCESS_CODE, INSPECT_ITEM_ID, ITEM_NAME, TIMING, INSPECT_METHOD, STATUS,
   PROD_QTY_AT_INSPECT, SAMPLE_NO, COMPANY, PLANT_CD, CREATED_BY, UPDATED_BY, CREATED_AT, UPDATED_AT, REMARK)
SELECT
  REGEXP_REPLACE(LOWER(RAWTOHEX(SYS_GUID())),'(.{8})(.{4})(.{4})(.{4})(.{12})','\1-\2-\3-\4-\5'),
  'JO-20260609-837', 'PRC-CRIMP', 'C112CBFB52504CF08C14C1FDCFF34260', '인장강도 시험 (Pull test)', 'FIRST',
  'DELEGATE', 'PENDING', 50, 1, '40', '1000', 'kiosk', 'kiosk',
