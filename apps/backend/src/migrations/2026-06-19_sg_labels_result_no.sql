DECLARE
  v_count NUMBER;
BEGIN
  SELECT COUNT(*) INTO v_count FROM USER_TAB_COLUMNS
   WHERE TABLE_NAME = 'SG_LABELS' AND COLUMN_NAME = 'RESULT_NO';
  IF v_count = 0 THEN
    EXECUTE IMMEDIATE 'ALTER TABLE SG_LABELS ADD (RESULT_NO VARCHAR2(50))';
    EXECUTE IMMEDIATE 'CREATE INDEX IX_SG_LABELS_RESULT ON SG_LABELS (RESULT_NO)';
  END IF;

  EXECUTE IMMEDIATE q'[COMMENT ON COLUMN SG_LABELS.RESULT_NO IS '발행 생산실적 번호(배치 단위 추적·멱등 키)']';
END;
/
