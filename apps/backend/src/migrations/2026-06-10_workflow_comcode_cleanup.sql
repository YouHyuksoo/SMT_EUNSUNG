-- 1. JUDGE_YN 공통코드 그룹 추가 (Y=합격, N=불합격)
-- 2. INSPECT_TYPE 그룹에 INITIAL/RETEST 추가
-- 3. IQC_LOGS 컬럼 코멘트 정리 (PASS/FAIL 직접 명시)
-- 대상: JSHANES (company=40 / plant=1000)
-- =====================================================================

-- 1) JUDGE_YN 공통코드 등록 (멱등)
MERGE INTO COM_CODES t
USING (
  SELECT 'JUDGE_YN' AS GROUP_CODE, 'Y' AS DETAIL_CODE, '합격' AS CODE_NAME,
         1 AS SORT_ORDER, 'Y' AS USE_YN,
         '40' AS COMPANY, '1000' AS PLANT_CD FROM DUAL
) s
ON (t.GROUP_CODE = s.GROUP_CODE AND t.DETAIL_CODE = s.DETAIL_CODE AND t.COMPANY = s.COMPANY AND t.PLANT_CD = s.PLANT_CD)
WHEN MATCHED THEN UPDATE SET t.CODE_NAME = s.CODE_NAME, t.SORT_ORDER = s.SORT_ORDER, t.USE_YN = s.USE_YN
WHEN NOT MATCHED THEN
  INSERT (GROUP_CODE, DETAIL_CODE, CODE_NAME, SORT_ORDER, USE_YN, COMPANY, PLANT_CD, CREATED_BY)
  VALUES (s.GROUP_CODE, s.DETAIL_CODE, s.CODE_NAME, s.SORT_ORDER, s.USE_YN, s.COMPANY, s.PLANT_CD, 'SYSTEM');
/

