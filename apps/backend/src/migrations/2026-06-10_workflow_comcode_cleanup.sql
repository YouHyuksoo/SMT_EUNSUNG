/

COMMENT ON COLUMN IQC_LOGS.INSPECT_TYPE IS '검사유형 (INITIAL=초기검사, RETEST=재검사, CONTINUITY=도통, INSULATION=절연, HI_POT=내압, VISUAL=외관)';
