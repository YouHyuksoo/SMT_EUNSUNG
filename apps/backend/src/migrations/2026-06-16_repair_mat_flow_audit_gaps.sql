-- Repair historical audit gaps after MAT arrival stock split.
-- Scope: JSHANES company 40 / plant 1000 historical test data only.
-- This migration does not change current stock quantities.

INSERT INTO MAT_ARRIVAL_TRANSACTIONS (
  TRANS_NO, TRANS_TYPE, TRANS_DATE,
  ARRIVAL_NO, ARRIVAL_SEQ, WAREHOUSE_CODE,
  ITEM_CODE, MAT_UID, QTY,
  UNIT_PRICE, TOTAL_AMOUNT,
  REF_TYPE, REF_ID, CANCEL_REF_ID,
  WORKER_ID, REMARK, STATUS,
  COMPANY, PLANT_CD, CREATED_BY, UPDATED_BY, CREATED_AT, UPDATED_AT
)
SELECT
  'TXFIX-ARR-' || st.MAT_UID,
  'ARRIVAL_IN',
  MIN(st.TRANS_DATE) - INTERVAL '1' MINUTE,
  ml.ARRIVAL_NO,
  ml.ARRIVAL_SEQ,
  NVL(MAX(st.FROM_WAREHOUSE_ID), MAX(st.TO_WAREHOUSE_ID)),
  st.ITEM_CODE,
  st.MAT_UID,
  SUM(st.QTY),
  NULL,
  NULL,
  'ARRIVAL_REPAIR',
  ml.ARRIVAL_NO,
  NULL,
  'MIGRATION',
  '입하재고 분리 후 과거 입고 LOT의 누락 입하원장 보강',
  'DONE',
  st.COMPANY,
  st.PLANT_CD,
  'MIGRATION',
  'MIGRATION',
  SYSTIMESTAMP,
  SYSTIMESTAMP
FROM STOCK_TRANSACTIONS st
JOIN MAT_LOTS ml
  ON ml.COMPANY = st.COMPANY
 AND ml.PLANT_CD = st.PLANT_CD
 AND ml.MAT_UID = st.MAT_UID
WHERE st.COMPANY = '40'
  AND st.PLANT_CD = '1000'
  AND st.TRANS_TYPE = 'RECEIVE'
  AND st.STATUS = 'DONE'
  AND NOT EXISTS (
    SELECT 1
    FROM MAT_ARRIVAL_TRANSACTIONS atx
    WHERE atx.COMPANY = st.COMPANY
      AND atx.PLANT_CD = st.PLANT_CD
      AND atx.MAT_UID = st.MAT_UID
      AND atx.ITEM_CODE = st.ITEM_CODE
      AND atx.TRANS_TYPE = 'ARRIVAL_IN'
  )
GROUP BY st.COMPANY, st.PLANT_CD, st.MAT_UID, st.ITEM_CODE, ml.ARRIVAL_NO, ml.ARRIVAL_SEQ
/

INSERT INTO MAT_RECEIVINGS (
  RECEIVE_NO, SEQ, MAT_UID, ITEM_CODE, QTY, WAREHOUSE_CODE,
  RECEIVE_DATE, WORKER_ID, REMARK, STATUS,
  COMPANY, PLANT_CD, CREATED_BY, CREATED_AT, UPDATED_BY, UPDATED_AT,
  ARRIVAL_NO, ARRIVAL_SEQ, VENDOR_BARCODE
)
SELECT
  'RCVFIX-' || atx.MAT_UID,
  1,
  atx.MAT_UID,
  atx.ITEM_CODE,
  SUM(atx.QTY),
  atx.WAREHOUSE_CODE,
  MIN(atx.TRANS_DATE) + INTERVAL '1' MINUTE,
  'MIGRATION',
  '입하재고 분리 후 과거 출고 LOT의 누락 입고이력 보강',
  'DONE',
  atx.COMPANY,
  atx.PLANT_CD,
  'MIGRATION',
  SYSTIMESTAMP,
  'MIGRATION',
  SYSTIMESTAMP,
  atx.ARRIVAL_NO,
  atx.ARRIVAL_SEQ,
  NULL
FROM MAT_ARRIVAL_TRANSACTIONS atx
WHERE atx.COMPANY = '40'
  AND atx.PLANT_CD = '1000'
  AND atx.TRANS_TYPE = 'ARRIVAL_IN'
  AND atx.STATUS = 'DONE'
  AND NOT EXISTS (
    SELECT 1
    FROM STOCK_TRANSACTIONS st
    WHERE st.COMPANY = atx.COMPANY
      AND st.PLANT_CD = atx.PLANT_CD
      AND st.MAT_UID = atx.MAT_UID
      AND st.ITEM_CODE = atx.ITEM_CODE
      AND st.TRANS_TYPE = 'RECEIVE'
      AND st.STATUS = 'DONE'
  )
  AND EXISTS (
    SELECT 1
    FROM STOCK_TRANSACTIONS mo
    WHERE mo.COMPANY = atx.COMPANY
      AND mo.PLANT_CD = atx.PLANT_CD
      AND mo.MAT_UID = atx.MAT_UID
      AND mo.ITEM_CODE = atx.ITEM_CODE
      AND mo.TRANS_TYPE = 'MAT_OUT'
      AND mo.STATUS = 'DONE'
  )
  AND NOT EXISTS (
    SELECT 1
    FROM MAT_RECEIVINGS mr
    WHERE mr.COMPANY = atx.COMPANY
      AND mr.PLANT_CD = atx.PLANT_CD
      AND mr.MAT_UID = atx.MAT_UID
      AND mr.ITEM_CODE = atx.ITEM_CODE
      AND mr.STATUS = 'DONE'
  )
GROUP BY atx.COMPANY, atx.PLANT_CD, atx.MAT_UID, atx.ITEM_CODE, atx.WAREHOUSE_CODE, atx.ARRIVAL_NO, atx.ARRIVAL_SEQ
/

INSERT INTO STOCK_TRANSACTIONS (
  TRANS_NO, TRANS_TYPE, TRANS_DATE,
  FROM_WAREHOUSE_ID, TO_WAREHOUSE_ID,
  ITEM_CODE, MAT_UID, QTY,
  UNIT_PRICE, TOTAL_AMOUNT,
  REF_TYPE, REF_ID, CANCEL_REF_ID,
  WORKER_ID, REMARK, STATUS,
  COMPANY, PLANT_CD, CREATED_BY, UPDATED_BY, CREATED_AT, UPDATED_AT
)
SELECT
  'TXFIX-RCV-' || mr.MAT_UID,
  'RECEIVE',
  mr.RECEIVE_DATE,
  mr.WAREHOUSE_CODE,
  mr.WAREHOUSE_CODE,
  mr.ITEM_CODE,
  mr.MAT_UID,
  mr.QTY,
  NULL,
  NULL,
  'RECEIVE_REPAIR',
  mr.RECEIVE_NO || '-' || mr.SEQ,
  NULL,
  'MIGRATION',
  '입하재고 분리 후 과거 출고 LOT의 누락 입고원장 보강',
  'DONE',
  mr.COMPANY,
  mr.PLANT_CD,
  'MIGRATION',
  'MIGRATION',
  SYSTIMESTAMP,
  SYSTIMESTAMP
FROM MAT_RECEIVINGS mr
WHERE mr.COMPANY = '40'
  AND mr.PLANT_CD = '1000'
  AND mr.RECEIVE_NO LIKE 'RCVFIX-%'
  AND NOT EXISTS (
    SELECT 1
    FROM STOCK_TRANSACTIONS st
    WHERE st.COMPANY = mr.COMPANY
      AND st.PLANT_CD = mr.PLANT_CD
      AND st.MAT_UID = mr.MAT_UID
      AND st.ITEM_CODE = mr.ITEM_CODE
      AND st.TRANS_TYPE = 'RECEIVE'
      AND st.STATUS = 'DONE'
  )
/
