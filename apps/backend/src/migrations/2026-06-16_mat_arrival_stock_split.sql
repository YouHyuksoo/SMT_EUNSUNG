INSERT INTO MAT_STOCKS_BAK_20260616
SELECT ms.*
  FROM MAT_STOCKS ms
 WHERE EXISTS (
       WITH tx AS (
         SELECT COMPANY,
                PLANT_CD,
                ITEM_CODE,
                MAT_UID,
                SUM(CASE WHEN TRANS_TYPE IN ('MAT_IN', 'MAT_IN_CANCEL') THEN QTY ELSE 0 END) AS ARRIVAL_QTY,
                SUM(CASE WHEN TRANS_TYPE = 'RECEIVE' THEN QTY ELSE 0 END) AS RECEIVE_QTY
           FROM STOCK_TRANSACTIONS
          WHERE TRANS_TYPE IN ('MAT_IN', 'MAT_IN_CANCEL', 'RECEIVE')
            AND MAT_UID IS NOT NULL
          GROUP BY COMPANY, PLANT_CD, ITEM_CODE, MAT_UID
       ),
       pending AS (
         SELECT COMPANY,
                PLANT_CD,
                ITEM_CODE,
                MAT_UID,
                ARRIVAL_QTY - RECEIVE_QTY AS PENDING_QTY
           FROM tx
          WHERE ARRIVAL_QTY - RECEIVE_QTY > 0
       ),
       ranked_stock AS (
         SELECT COMPANY,
                PLANT_CD,
                WAREHOUSE_CODE,
                ITEM_CODE,
                MAT_UID,
                QTY,
                AVAILABLE_QTY,
                ROW_NUMBER() OVER (
                  PARTITION BY COMPANY, PLANT_CD, ITEM_CODE, MAT_UID
                  ORDER BY QTY DESC, AVAILABLE_QTY DESC, WAREHOUSE_CODE
                ) AS RN
           FROM MAT_STOCKS
          WHERE QTY > 0
            AND AVAILABLE_QTY > 0
       ),
       candidates AS (
         SELECT p.COMPANY,
                p.PLANT_CD,
                rs.WAREHOUSE_CODE,
                p.ITEM_CODE,
                p.MAT_UID,
                LEAST(p.PENDING_QTY, rs.QTY, rs.AVAILABLE_QTY) AS SAFE_QTY
           FROM pending p
           JOIN ranked_stock rs
             ON rs.COMPANY = p.COMPANY
            AND rs.PLANT_CD = p.PLANT_CD
            AND rs.ITEM_CODE = p.ITEM_CODE
            AND rs.MAT_UID = p.MAT_UID
            AND rs.RN = 1
       )
       SELECT 1
         FROM candidates c
        WHERE c.SAFE_QTY > 0
          AND c.COMPANY = ms.COMPANY
          AND c.PLANT_CD = ms.PLANT_CD
          AND c.WAREHOUSE_CODE = ms.WAREHOUSE_CODE
          AND c.ITEM_CODE = ms.ITEM_CODE
          AND c.MAT_UID = ms.MAT_UID
     )
   AND NOT EXISTS (
         SELECT 1
           FROM MAT_STOCKS_BAK_20260616 b
          WHERE b.COMPANY = ms.COMPANY
            AND b.PLANT_CD = ms.PLANT_CD
            AND b.WAREHOUSE_CODE = ms.WAREHOUSE_CODE
            AND b.ITEM_CODE = ms.ITEM_CODE
            AND b.MAT_UID = ms.MAT_UID
