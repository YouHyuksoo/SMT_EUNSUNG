MERGE INTO MAT_STOCKS ms
USING (
  WITH tx AS (
    SELECT COMPANY,
           PLANT_CD,
           ITEM_CODE,
           MAT_UID,
           SUM(CASE WHEN TRANS_TYPE IN ('MAT_IN', 'MAT_IN_CANCEL') THEN QTY ELSE 0 END) AS ARRIVAL_QTY,
           SUM(CASE WHEN TRANS_TYPE = 'RECEIVE' THEN QTY ELSE 0 END) AS RECEIVE_QTY
      FROM STOCK_TRANSACTIONS
     WHERE TRANS_TYPE IN ('MAT_IN', 'MAT_IN_CANCEL', 'RECEIVE')
       AND MAT_UID IS NOT NULL
     GROUP BY COMPANY, PLANT_CD, ITEM_CODE, MAT_UID
  ),
  pending AS (
    SELECT COMPANY,
           PLANT_CD,
           ITEM_CODE,
           MAT_UID,
           ARRIVAL_QTY - RECEIVE_QTY AS PENDING_QTY
      FROM tx
     WHERE ARRIVAL_QTY - RECEIVE_QTY > 0
  ),
  ranked_stock AS (
    SELECT COMPANY,
           PLANT_CD,
           WAREHOUSE_CODE,
           ITEM_CODE,
           MAT_UID,
           QTY,
           AVAILABLE_QTY,
           ROW_NUMBER() OVER (
             PARTITION BY COMPANY, PLANT_CD, ITEM_CODE, MAT_UID
             ORDER BY QTY DESC, AVAILABLE_QTY DESC, WAREHOUSE_CODE
           ) AS RN
      FROM MAT_STOCKS
     WHERE QTY > 0
       AND AVAILABLE_QTY > 0
  )
  SELECT p.COMPANY,
         p.PLANT_CD,
         rs.WAREHOUSE_CODE,
         p.ITEM_CODE,
         p.MAT_UID,
         LEAST(p.PENDING_QTY, rs.QTY, rs.AVAILABLE_QTY) AS SAFE_QTY
    FROM pending p
    JOIN ranked_stock rs
      ON rs.COMPANY = p.COMPANY
     AND rs.PLANT_CD = p.PLANT_CD
     AND rs.ITEM_CODE = p.ITEM_CODE
     AND rs.MAT_UID = p.MAT_UID
     AND rs.RN = 1
) c
ON (
  ms.COMPANY = c.COMPANY
  AND ms.PLANT_CD = c.PLANT_CD
  AND ms.WAREHOUSE_CODE = c.WAREHOUSE_CODE
  AND ms.ITEM_CODE = c.ITEM_CODE
  AND ms.MAT_UID = c.MAT_UID
)
WHEN MATCHED THEN UPDATE SET
  ms.QTY = ms.QTY - c.SAFE_QTY,
  ms.AVAILABLE_QTY = ms.AVAILABLE_QTY - c.SAFE_QTY,
  ms.UPDATED_BY = 'arrival-split-migration',
  ms.UPDATED_AT = SYSTIMESTAMP
WHERE c.SAFE_QTY > 0;
/

