MERGE INTO ROUTING_PROCESSES p
  USING (
    SELECT r.COMPANY, r.PLANT_CD, r.ROUTING_CODE, r.SEQ, r.PROCESS_CODE,
           pm.PROCESS_NAME, pm.PROCESS_TYPE, r.STD_TIME
    FROM (
      SELECT '40' COMPANY, '1000' PLANT_CD, 'N91H00-X9800' ROUTING_CODE, 10 SEQ, 'MASSY' PROCESS_CODE, 30 STD_TIME FROM DUAL
      UNION ALL SELECT '40', '1000', 'N91H00-X9800-S', 10, 'TUBHT', 20 FROM DUAL
      UNION ALL SELECT '40', '1000', 'N91H00-X9800-S-A', 10, 'CRMPB', 24 FROM DUAL
