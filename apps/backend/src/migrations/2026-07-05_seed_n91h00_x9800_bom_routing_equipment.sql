-- Seed N91H00-X9800 default model so production order creation can expand BOM,
-- create semi-product child orders, and show equipment candidates per process.

MERGE INTO BOM_MASTERS b
  USING (
    SELECT
      COMPANY,
      PLANT_CD,
      'N91H00-X9800' AS PARENT_ITEM_CODE,
      CHILD_ITEM_CODE,
      QTY_PER,
      SEQ,
      REVISION,
      BOM_GRP,
      OPER,
      SIDE,
      ECO_NO,
      VALID_FROM,
      VALID_TO,
      'Y' AS USE_YN,
      'N91H00-X9800 기본 품목 BOM seed' AS REMARK
    FROM BOM_MASTERS
    WHERE COMPANY = '40'
      AND PLANT_CD = '1000'
      AND PARENT_ITEM_CODE = 'N91H00-X9800_1'
      AND USE_YN = 'Y'
  ) s
  ON (
    b.COMPANY = s.COMPANY
    AND b.PLANT_CD = s.PLANT_CD
    AND b.PARENT_ITEM_CODE = s.PARENT_ITEM_CODE
    AND b.CHILD_ITEM_CODE = s.CHILD_ITEM_CODE
    AND b.REVISION = s.REVISION
  )
  WHEN MATCHED THEN UPDATE SET
    b.QTY_PER = s.QTY_PER,
    b.SEQ = s.SEQ,
    b.BOM_GRP = s.BOM_GRP,
    b.OPER = s.OPER,
    b.SIDE = s.SIDE,
    b.ECO_NO = s.ECO_NO,
    b.VALID_FROM = s.VALID_FROM,
    b.VALID_TO = s.VALID_TO,
    b.USE_YN = 'Y',
    b.REMARK = s.REMARK,
    b.UPDATED_BY = 'codex',
    b.UPDATED_AT = SYSTIMESTAMP
  WHEN NOT MATCHED THEN INSERT (
    COMPANY,
    PLANT_CD,
    PARENT_ITEM_CODE,
    CHILD_ITEM_CODE,
    QTY_PER,
    SEQ,
    REVISION,
    BOM_GRP,
    OPER,
    SIDE,
    ECO_NO,
    VALID_FROM,
    VALID_TO,
    USE_YN,
    REMARK,
    CREATED_BY,
    UPDATED_BY,
    CREATED_AT,
    UPDATED_AT
  ) VALUES (
    s.COMPANY,
    s.PLANT_CD,
    s.PARENT_ITEM_CODE,
    s.CHILD_ITEM_CODE,
