DECLARE
  v_count NUMBER;
BEGIN
  SELECT COUNT(*)
    INTO v_count
    FROM USER_TAB_COLUMNS
   WHERE TABLE_NAME = 'PRODUCT_STOCKS'
     AND COLUMN_NAME = 'QUALITY_STATUS';

  IF v_count = 0 THEN
    EXECUTE IMMEDIATE 'ALTER TABLE PRODUCT_STOCKS ADD (QUALITY_STATUS VARCHAR2(20) DEFAULT ''GOOD'' NOT NULL)';
  END IF;
END;
