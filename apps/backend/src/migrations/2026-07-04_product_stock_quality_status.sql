DECLARE
  v_count NUMBER;
  v_pk_count NUMBER;
BEGIN
  SELECT COUNT(*)
    INTO v_count
    FROM USER_CONS_COLUMNS
   WHERE TABLE_NAME = 'PRODUCT_STOCKS'
     AND CONSTRAINT_NAME = 'PK_PRODUCT_STOCKS'
     AND COLUMN_NAME = 'QUALITY_STATUS';

  IF v_count = 0 THEN
    SELECT COUNT(*)
      INTO v_pk_count
      FROM USER_CONSTRAINTS
     WHERE TABLE_NAME = 'PRODUCT_STOCKS'
       AND CONSTRAINT_NAME = 'PK_PRODUCT_STOCKS';

    IF v_pk_count > 0 THEN
      EXECUTE IMMEDIATE 'ALTER TABLE PRODUCT_STOCKS DROP CONSTRAINT PK_PRODUCT_STOCKS';
    END IF;

    EXECUTE IMMEDIATE 'ALTER TABLE PRODUCT_STOCKS ADD CONSTRAINT PK_PRODUCT_STOCKS PRIMARY KEY (COMPANY, PLANT_CD, WAREHOUSE_CODE, ITEM_CODE, QUALITY_STATUS)';
  END IF;
END;
