BEGIN
  /*
   * 2026-05-26: IQC005 Phase A — 데모용 PO 시드 (사용자 검증 시나리오 지원)
   * 6 PO / 9 라인. 4단계 행 색상이 모두 보이도록 lineStatus 분포 설정:
   *   - OPEN (미입하, 흰)        : 4 라인
   *   - PARTIAL (일부입하, 노란)  : 3 라인
   *   - CLOSE (잔량 0 + CLOSE, 회색): 2 라인
   * USE_TYPE: PROD(양산) 4건 + DEV(개발) 2건.
   * 멱등성: MERGE 사용, 기존 동일 poNo는 갱신.
   */

  -- ── PO 헤더 6건 ──────────────────────────────────────────
  MERGE INTO PURCHASE_ORDERS p
  USING (
    SELECT 'PO-26-001' AS PO_NO, 'VND-001' AS PARTNER_ID, '한국단자공업' AS PARTNER_NAME,
           DATE '2026-05-15' AS ORDER_DATE, DATE '2026-05-30' AS DUE_DATE, 'PROD' AS USE_TYPE FROM DUAL UNION ALL
    SELECT 'PO-26-002', 'VND-002', '대한전선', DATE '2026-05-18', DATE '2026-06-02', 'PROD' FROM DUAL UNION ALL
    SELECT 'PO-26-003', 'VND-003', '삼화부자재', DATE '2026-05-20', DATE '2026-06-04', 'PROD' FROM DUAL UNION ALL
    SELECT 'PO-26-004', 'VND-001', '한국단자공업', DATE '2026-04-21', DATE '2026-05-06', 'PROD' FROM DUAL UNION ALL
    SELECT 'PO-26-005', 'VND-002', '대한전선', DATE '2026-04-08', DATE '2026-04-25', 'DEV' FROM DUAL UNION ALL
    SELECT 'PO-26-006', 'VND-003', '삼화부자재', DATE '2026-05-22', DATE '2026-06-07', 'DEV' FROM DUAL
  ) s ON (p.PO_NO = s.PO_NO)
  WHEN MATCHED THEN UPDATE SET
    PARTNER_ID = s.PARTNER_ID,
    PARTNER_NAME = s.PARTNER_NAME,
    ORDER_DATE = s.ORDER_DATE,
    DUE_DATE = s.DUE_DATE,
    STATUS = 'CONFIRMED',
    USE_TYPE = s.USE_TYPE
  WHEN NOT MATCHED THEN INSERT
    (PO_NO, PARTNER_ID, PARTNER_NAME, ORDER_DATE, DUE_DATE, STATUS, USE_TYPE,
     COMPANY, PLANT_CD, CREATED_BY)
  VALUES
    (s.PO_NO, s.PARTNER_ID, s.PARTNER_NAME, s.ORDER_DATE, s.DUE_DATE, 'CONFIRMED', s.USE_TYPE,
     '40', '1000', 'SYSTEM');

  -- ── PO 라인 9건 (poNo + seq 복합 PK) ─────────────────────
  -- 컬럼: PO_NO, SEQ, ITEM_CODE, ORDER_QTY, RECEIVED_QTY, LINE_NO, REV_NO, LINE_STATUS

  MERGE INTO PURCHASE_ORDER_ITEMS pi
  USING (
    -- PO-26-001 양산 (한국단자공업) — 라인 2건: 일부입하 + 미입하
    SELECT 'PO-26-001' AS PO_ID, 1 AS SEQ, 'RM-TERM-001' AS ITEM_CODE, 1000 AS ORDER_QTY,
           300 AS RECEIVED_QTY, 1 AS LINE_NO, 1 AS REV_NO, 'PARTIAL' AS LINE_STATUS FROM DUAL UNION ALL
    SELECT 'PO-26-001', 2, 'RM-TERM-002', 800, 0, 2, 1, 'OPEN' FROM DUAL UNION ALL
    -- PO-26-002 양산 (대한전선) — 라인 1건: 일부입하
    SELECT 'PO-26-002', 1, 'RM-WIRE-001', 500, 200, 1, 1, 'PARTIAL' FROM DUAL UNION ALL
    -- PO-26-003 양산 (삼화부자재) — 라인 1건: 미입하
    SELECT 'PO-26-003', 1, 'RM-SEAL-001', 2000, 0, 1, 1, 'OPEN' FROM DUAL UNION ALL
    -- PO-26-004 양산 (한국단자공업) — 라인 1건: 잔량 0 CLOSE
    SELECT 'PO-26-004', 1, 'RM-TERM-001', 500, 500, 1, 1, 'CLOSE' FROM DUAL UNION ALL
    -- PO-26-005 개발 (대한전선) — 라인 2건: CLOSE + 미입하
    SELECT 'PO-26-005', 1, 'RM-SEAL-001', 1500, 900, 1, 1, 'CLOSE' FROM DUAL UNION ALL
    SELECT 'PO-26-005', 2, 'RM-TAPE-001', 50, 0, 2, 1, 'OPEN' FROM DUAL UNION ALL
    -- PO-26-006 개발 (삼화부자재) — 라인 2건: 일부입하 + 미입하
    SELECT 'PO-26-006', 1, 'RM-CONN-001', 300, 100, 1, 1, 'PARTIAL' FROM DUAL UNION ALL
    SELECT 'PO-26-006', 2, 'RM-GROM-001', 400, 0, 2, 1, 'OPEN' FROM DUAL
  ) s ON (pi.PO_ID = s.PO_ID AND pi.SEQ = s.SEQ)
  WHEN MATCHED THEN UPDATE SET
    ITEM_CODE = s.ITEM_CODE,
    ORDER_QTY = s.ORDER_QTY,
    RECEIVED_QTY = s.RECEIVED_QTY,
    LINE_NO = s.LINE_NO,
    REV_NO = s.REV_NO,
    LINE_STATUS = s.LINE_STATUS
  WHEN NOT MATCHED THEN INSERT
    (PO_ID, SEQ, ITEM_CODE, ORDER_QTY, RECEIVED_QTY, LINE_NO, REV_NO, LINE_STATUS,
     COMPANY, PLANT_CD, CREATED_BY)
  VALUES
    (s.PO_ID, s.SEQ, s.ITEM_CODE, s.ORDER_QTY, s.RECEIVED_QTY, s.LINE_NO, s.REV_NO, s.LINE_STATUS,
     '40', '1000', 'SYSTEM');

  COMMIT;
END;
