CREATE OR REPLACE FUNCTION
  /* ================================================================
   * 함수명  : F_GET_MONTH_CODE
   * 작성자  : 지성솔루션컨설팅
   * 작성일  : 2026-07-02
   * 수정이력:
   *   2026-07-02 - 지성솔루션컨설팅 - 최초 작성
   *   2026-07-02 - AI(Codex)       - 한글 주석 자동 추가
   * ================================================================
   * [AI 분석] 기능 설명:
   *   입력 파라미터와 기준 테이블을 이용해 업무 코드, 명칭, 수량, 상태 등의 조회 값을 반환한다.
   *   조회 실패 또는 예외 상황에서는 원본 로직에 정의된 기본값/NULL/오류 처리를 따른다.
   * ================================================================
   * [AI 분석] 파라미터:
   *   P_MODEL_NAME  (IN, VARCHAR2) - 모델 / 명칭 관련 값
   *   P_YYYYMM  (IN, VARCHAR2) - 함수 입력값
   * ================================================================
   * [AI 분석] 반환값:
   *   VARCHAR2 - 함수 처리 결과값
   * ================================================================
   * [AI 분석] 참조 테이블:
   *   IP_PRODUCT_YEAR_BASE - 제품 관련 값 조회 또는 참조
   * ================================================================
   * [AI 분석] 예외 처리:
   *   WHEN 절 또는 원본 예외 로직 기준으로 기본값 반환/애플리케이션 오류 처리를 수행한다.
   * ================================================================
   * [AI 분석] 복잡도:
   *   조건 분기: IF 0회 / 반복문: 0회
   *   DML: SELECT 2회
   * ================================================================
   * 사용 예시:
   *   SELECT F_GET_MONTH_CODE(...) FROM DUAL;
   * ================================================================ */
 "F_GET_MONTH_CODE" (
   p_model_name   IN VARCHAR2,
   p_yyyymm       IN VARCHAR2)
   RETURN VARCHAR2
IS
   lvs_return   VARCHAR2 (1);
BEGIN
   SELECT DECODE (SUBSTR (p_yyyymm, 5, 2),
                  '01', m1,
                  '02', m2,
                  '03', m3,
                  '04', m4,
                  '05', m5,
                  '06', m6,
                  '07', m7,
                  '08', m8,
                  '09', m9,
                  '10', m10,
                  '11', m11,
                  '12', m12,
                  '*')
     INTO lvs_return
     FROM ip_product_year_base
    WHERE model_name = p_model_name AND yyyy = SUBSTR (p_yyyymm, 1, 4);

   RETURN lvs_return;
EXCEPTION
   WHEN NO_DATA_FOUND
   THEN
      SELECT DECODE (TO_CHAR (SYSDATE, 'MM'),
                     '01', '1',
                     '02', '2',
                     '03', '3',
                     '04', '4',
                     '05', '5',
                     '06', '6',
                     '07', '7',
                     '08', '8',
                     '09', '9',
                     '10', 'A',
                     '11', 'B',
                     '12', 'C',
                     'Z')
        INTO lvs_return
        FROM DUAL;

      RETURN lvs_return;
END;
