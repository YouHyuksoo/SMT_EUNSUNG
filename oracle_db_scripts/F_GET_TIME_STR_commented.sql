CREATE OR REPLACE FUNCTION
  /* ================================================================
   * 함수명  : F_GET_TIME_STR
   * 작성자  : 지성솔루션컨설팅
   * 작성일  : 2026-07-02
   * 수정이력:
   *   2026-07-02 - 지성솔루션컨설팅 - 최초 작성
   *   2026-07-02 - AI(Codex)       - 한글 주석 자동 추가
   * ================================================================
   * [AI 분석] 기능 설명:
   *   입력 파라미터와 기준 테이블을 이용해 업무 코드, 명칭, 수량, 상태 등의 조회 값을 반환한다.
   *   조회 실패 또는 예외 상황에서는 원본 로직에 정의된 기본값/NULL/오류 처리를 따른다.
   * ================================================================
   * [AI 분석] 파라미터:
   *   P_TIME  (IN, NUMBER) - 시간 관련 값
   * ================================================================
   * [AI 분석] 반환값:
   *   VARCHAR2 - 함수 처리 결과값
   * ================================================================
   * [AI 분석] 참조 테이블:
   *   없음 (순수 연산 함수 또는 원본 소스 기준 명시 테이블 없음)
   * ================================================================
   * [AI 분석] 예외 처리:
   *   WHEN 절 또는 원본 예외 로직 기준으로 기본값 반환/애플리케이션 오류 처리를 수행한다.
   * ================================================================
   * [AI 분석] 복잡도:
   *   조건 분기: IF 2회 / 반복문: 0회
   *   DML: SELECT 1회
   * ================================================================
   * 사용 예시:
   *   SELECT F_GET_TIME_STR(...) FROM DUAL;
   * ================================================================ */
 "F_GET_TIME_STR" (p_time  IN NUMBER)
    RETURN VARCHAR2
IS
    lvs_time_set   VARCHAR2 (20);
BEGIN
  
    IF p_time = 0 THEN
       RETURN '';
    END IF;
    
    select TRIM(TO_CHAR(floor(p_time),'900'))||':'|| TRIM(TO_CHAR(floor(mod(p_time,1)*60),'00'))
       into lvs_time_set
       from dual ;
   
    RETURN lvs_time_set;
    
EXCEPTION
    WHEN otherS THEN
         RETURN '';
END;
