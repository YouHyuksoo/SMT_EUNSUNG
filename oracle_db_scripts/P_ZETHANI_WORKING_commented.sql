CREATE OR REPLACE PROCEDURE "P_ZETHANI_WORKING"
  /* ================================================================
   * 프로시저명  : P_ZETHANI_WORKING
   * 작성자  : 지성솔루션컨설팅
   * 작성일  : 2020-07-30
   * 수정이력:
   *   2020-07-30 - 지성솔루션컨설팅 - 최초 작성
   *   2026-07-01 - AI(Codex)       - 한글 주석 자동 추가
   * ================================================================
   * [AI 분석] 기능 설명:
   *   라벨 프린터 명령 문자열을 조합하여 DBMS_OUTPUT으로 출력하는 테스트성 프로시저이다.
   *   SIZE, GAP, DIRECTION, TEXT, DMATRIX 명령을 순서대로 이어 붙여 바코드 라벨 명령을 만든다.
   *   테이블 접근이나 업무 데이터 변경 없이 출력 문자열 생성만 수행한다.
   * ================================================================
   * [AI 분석] 파라미터:
   *   없음
   * ================================================================
   * [AI 분석] 참조 테이블:
   *   없음 (순수 문자열 조합 프로시저)
   * ================================================================
   * [AI 분석] 예외 처리:
   *   별도 EXCEPTION 절 없음 - 오류 발생 시 호출부로 전달된다.
   * ================================================================
   * [AI 분석] 복잡도:
   *   조건 분기: IF 0회 / 반복문: 없음
   *   DML: 없음
   * ================================================================
   * 사용 예시:
   *   EXEC P_ZETHANI_WORKING
   * ================================================================ */
is
  lvs_barcode varchar2(4000); -- [AI] 라벨 프린터 명령 전체 문자열을 누적하는 변수
  CRLF varchar2(20) := CHR(13); -- [AI] 프린터 명령 줄 구분자로 사용할 캐리지 리턴 값
begin
  

    
    -- [AI] 라벨 출력 기본 설정 명령을 순서대로 조합한다.
    lvs_barcode := 'SIZE 44 mm,13 mm' || CRLF;
    lvs_barcode := lvs_barcode || 'GAP 3 mm' || CRLF;
    lvs_barcode := lvs_barcode || 'DIRECTION 1' || CRLF;
    lvs_barcode := lvs_barcode || 'SHIFT -25' || CRLF;
    lvs_barcode := lvs_barcode || 'OFFSET 0' || CRLF;
    lvs_barcode := lvs_barcode || 'CLS' || CRLF;
    -- [AI] 라벨 텍스트와 2D 바코드 출력 명령을 조합한다.
    lvs_barcode := lvs_barcode || 'TEXT 78,49, "B.FNT",0,1,2,' || '"' || '20170302' || '"' || CRLF;
    lvs_barcode := lvs_barcode || 'TEXT 78,74, "B.FNT",0,1,2, ' || '"' || 23 || to_char(23, 'FM0000') || '"' || CRLF;
    lvs_barcode := lvs_barcode || 'DMATRIX 13,44,10,10,' || '"' || 231321321321321321321 || '"' || CRLF;   
    
    
-- [AI] 조합된 프린터 명령 문자열을 서버 출력 버퍼에 기록한다.
dbms_output.put_line(lvs_barcode);



end P_ZETHANI_WORKING;
