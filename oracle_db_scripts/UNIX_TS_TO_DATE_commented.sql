CREATE OR REPLACE FUNCTION
  /* ================================================================
   * 함수명  : UNIX_TS_TO_DATE
   * 작성자  : 지성솔루션컨설팅
   * 작성일  : 2026-07-02
   * 수정이력:
   *   2026-07-02 - 지성솔루션컨설팅 - 최초 작성
   *   2026-07-02 - AI(Codex)       - 한글 주석 자동 추가
   * ================================================================
   * [AI 분석] 기능 설명:
   *   날짜/시간 값을 업무 기준 형식으로 변환하거나 기간 값을 계산하여 반환한다.
   *   반환 타입은 원본 RETURN 선언을 따른다.
   * ================================================================
   * [AI 분석] 파라미터:
   *   P_UNIX_DT  (IN, number) - 함수 입력값
   * ================================================================
   * [AI 분석] 반환값:
   *   date - 함수 처리 결과값
   * ================================================================
   * [AI 분석] 참조 테이블:
   *   없음 (순수 연산 함수 또는 원본 소스 기준 명시 테이블 없음)
   * ================================================================
   * [AI 분석] 예외 처리:
   *   명시적 EXCEPTION 블록 없음 - 호출부에서 표준 Oracle 예외를 처리한다.
   * ================================================================
   * [AI 분석] 복잡도:
   *   조건 분기: IF 0회 / 반복문: 0회
   *   DML: SELECT 1회
   * ================================================================
   * 사용 예시:
   *   SELECT UNIX_TS_TO_DATE(...) FROM DUAL;
   * ================================================================ */
                    "UNIX_TS_TO_DATE" (p_unix_dt number) return date is
  Result date;
begin

   SELECT TO_DATE('19700102','yyyymmdd') + (p_unix_dt/24/60/60)
     INTO Result
     FROM dual;

  return(Result);
end UNIX_TS_TO_DATE;
