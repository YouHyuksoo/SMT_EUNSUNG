   *   P_COLUMN_NAME  (IN, VARCHAR2) - 명칭 관련 값
   * ================================================================
   * [AI 분석] 반환값:
   *   VARCHAR2 - 함수 처리 결과값
   * ================================================================
   * [AI 분석] 참조 테이블:
   *   ISYS_DUAL_LANGUAGE - 업무 기준/거래 데이터 조회 또는 참조
   * ================================================================
   * [AI 분석] 예외 처리:
   *   명시적 EXCEPTION 블록 없음 - 호출부에서 표준 Oracle 예외를 처리한다.
   * ================================================================
   * [AI 분석] 복잡도:
   *   조건 분기: IF 0회 / 반복문: 0회
   *   DML: SELECT 1회
   * ================================================================
   * 사용 예시:
   *   SELECT F_GET_COLUMN_COMMENTS(...) FROM DUAL;
   * ================================================================ */
 F_GET_COLUMN_COMMENTS 
(
  P_COLUMN_NAME IN VARCHAR2 
) RETURN VARCHAR2 AS 

LVS_RETURN VARCHAR2(200) ;
BEGIN

  SELECT KOREA_TEXT INTO LVS_RETURN  FROM ISYS_DUAL_LANGUAGE
  WHERE UPPER(ENGLISH_TEXT)   = UPPER(REPLACE ( P_COLUMN_NAME , '_' , ' ' )) 
   AND ROWNUM = 1 ;

  RETURN LVS_RETURN ;
END F_GET_COLUMN_COMMENTS;
