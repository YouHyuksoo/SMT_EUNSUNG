   *   2026-07-02 - AI(Codex)       - 한글 주석 자동 추가
   * ================================================================
   * [AI 분석] 기능 설명:
   *   입력 조건 또는 기준 데이터의 존재/상태를 확인하여 검증 결과를 반환한다.
   *   화면, 설비, 인터락 로직에서 사전 체크용으로 호출되는 함수로 추정된다.
   * ================================================================
   * [AI 분석] 파라미터:
   *   P_PID  (IN, varchar2) - 제품 식별자
   * ================================================================
   * [AI 분석] 반환값:
   *   varchar2 - 함수 처리 결과값
   * ================================================================
   * [AI 분석] 참조 테이블:
   *   IP_PRODUCT_WORK_QC - 제품 관련 값 조회 또는 참조
   *   IP_PRODUCT_PACK_SERIAL - 제품 / 시리얼 관련 값 조회 또는 참조
   * ================================================================
   * [AI 분석] 예외 처리:
   *   명시적 EXCEPTION 블록 없음 - 호출부에서 표준 Oracle 예외를 처리한다.
   * ================================================================
   * [AI 분석] 복잡도:
   *   조건 분기: IF 4회 / 반복문: 0회
   *   DML: SELECT 2회
   * ================================================================
   * 사용 예시:
   *   SELECT F_CHECK_PID_STATUS_4_WS(...) FROM DUAL;
   * ================================================================ */
 "F_CHECK_PID_STATUS_4_WS" (p_pid varchar2) return varchar2 is

  lvi_defect number;
  lvi_pack number; 
  /**********************************************
  * PID 의 상태를 반환 한다. 
  * 1.수리품인지 
  * 2.패킹된 제품인지 2018.05.13
  ***********************************************/
begin

 select count(*)
   into lvi_defect
   from ip_product_work_qc
  where serial_no = p_pid    
    and receipt_deficit = 1  
  ;

 if lvi_defect > 0 then
   return '9204' ;   --수리실에서 미 출고된 시리얼 입니다. 
   --수리품 메시지 
 end if ;
 
 SELECT COUNT(*) 
   INTO lvi_pack 
   FROM IP_PRODUCT_PACK_SERIAL X 
  WHERE X.BARCODE = P_PID 
 ; 
 
 if lvi_pack > 0 then 
   return '9205';  --이미 패킹이된 시리얼 입니다. 
 end if ;
 
 return 'OK' ;


end F_CHECK_PID_STATUS_4_WS;
