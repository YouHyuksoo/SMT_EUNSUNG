CREATE OR REPLACE PROCEDURE P_JOB_PROFILE_RESET
  /* ================================================================
   * 프로시저명  : P_JOB_PROFILE_RESET
   * 작성자  : 지성솔루션컨설팅
   * 작성일  : 2020-07-30
   * 수정이력:
   *   2020-07-30 - 지성솔루션컨설팅 - 최초 작성
   *   2026-07-01 - AI(Codex)       - 한글 주석 자동 추가
   * ================================================================
   * [AI 분석] 기능 설명:
   *   제품 라인 마스터의 모델 스펙 확인 상태와 확인 일자를 초기화한다.
   *   전체 IP_PRODUCT_LINE 행을 대상으로 현재 스펙 확인 상태와 최근 스펙 확인 이력을 NULL로 변경한다.
   *   처리 후 COMMIT을 수행하므로 호출 즉시 변경 내용이 확정된다.
   * ================================================================
   * [AI 분석] 파라미터:
   *   없음
   * ================================================================
   * [AI 분석] 참조 테이블:
   *   IP_PRODUCT_LINE - Product Line Master
   *     처리: SPEC_CHECK_STATUS, SPEC_CHECK_DATE, LAST_SPEC_CHECK_STATUS, LAST_SPEC_CHECK_DATE 초기화
   * ================================================================
   * [AI 분석] 예외 처리:
   *   별도 EXCEPTION 절 없음 - 오류 발생 시 호출부로 전달되며 명시적 ROLLBACK은 없다.
   * ================================================================
   * [AI 분석] 복잡도:
   *   조건 분기: IF 0회 / 반복문: 없음
   *   DML: UPDATE 1회
   *   주의: COMMIT 포함
   * ================================================================
   * 사용 예시:
   *   EXEC P_JOB_PROFILE_RESET
   * ================================================================ */
IS

  
BEGIN
  
      -- [AI] 전체 제품 라인의 모델 스펙 확인 상태와 이력 정보를 초기화한다.
      UPDATE IP_PRODUCT_LINE
         SET SPEC_CHECK_STATUS        = NULL,
             SPEC_CHECK_DATE          = NULL,
             LAST_SPEC_CHECK_STATUS   = NULL,
             LAST_SPEC_CHECK_DATE     = NULL;
     
--  WHERE 1=2;
             
      -- [AI] 초기화 결과를 즉시 확정한다.
      COMMIT;
      
END;
