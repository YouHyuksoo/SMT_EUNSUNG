   * 수정이력:
   *   2026-07-02 - 지성솔루션컨설팅 - 최초 작성
   *   2026-07-02 - AI(Codex)       - 한글 주석 자동 추가
   * ================================================================
   * [AI 분석] 기능 설명:
   *   입력 파라미터와 기준 테이블을 이용해 업무 코드, 명칭, 수량, 상태 등의 조회 값을 반환한다.
   *   조회 실패 또는 예외 상황에서는 원본 로직에 정의된 기본값/NULL/오류 처리를 따른다.
   * ================================================================
   * [AI 분석] 파라미터:
   *   P_RUN_NO  (IN, VARCHAR2) - 작업지시/런 번호
   * ================================================================
   * [AI 분석] 반환값:
   *   NUMBER - 함수 처리 결과값
   * ================================================================
   * [AI 분석] 참조 테이블:
   *   IQ_MACHINE_INSPECT_DATA_AOI - 검사 관련 값 조회 또는 참조
   * ================================================================
   * [AI 분석] 예외 처리:
   *   WHEN 절 또는 원본 예외 로직 기준으로 기본값 반환/애플리케이션 오류 처리를 수행한다.
   * ================================================================
   * [AI 분석] 복잡도:
   *   조건 분기: IF 2회 / 반복문: 0회
   *   DML: SELECT 1회
   * ================================================================
   * 사용 예시:
   *   SELECT F_GET_AOI_PASS_RATE_BY_RUNNO(...) FROM DUAL;
   * ================================================================ */
 "F_GET_AOI_PASS_RATE_BY_RUNNO" (
   p_run_no       IN VARCHAR2 )
   RETURN NUMBER
IS
   lvf_rate     NUMBER;
   lvf_ng_qty   NUMBER;
   lvf_ok_qty   NUMBER;
BEGIN
---------------------------------------------------------------
-- AOI  Result 기준 OK, NG Count   : serial 중복 허용
---------------------------------------------------------------

    
   SELECT SUM (DECODE (NVL(RESULT, '*'), 'OK', 0, 'GOOD', 0, 'Good', 0, 1)) ng_qty,
          SUM (DECODE (NVL(RESULT, '*'), 'OK', 1, 'GOOD', 1, 'Good', 1, 0)) ok_qty
     INTO lvf_ng_qty, lvf_ok_qty
     FROM IQ_MACHINE_INSPECT_DATA_AOI
    WHERE RUN_NO = P_RUN_NO
      AND NVL(RESULT, '*') not in ( 'MasterOK', 'MasterNG');    
          
---------------------------------------------------------------
--
---------------------------------------------------------------

   IF NVL (lvf_ng_qty, 0) + NVL (lvf_ok_qty, 0) = 0
   THEN
      RETURN 0;
   ELSE
      lvf_rate := TRUNC (lvf_ok_qty / (lvf_ng_qty + lvf_ok_qty), 3) * 100;
   END IF;

   RETURN lvf_rate;

---------------------------------------------------------------
--
---------------------------------------------------------------

EXCEPTION
   WHEN OTHERS
   THEN
      RETURN 0;
END;
