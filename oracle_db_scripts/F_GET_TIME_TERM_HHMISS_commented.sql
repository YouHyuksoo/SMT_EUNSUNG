CREATE OR REPLACE FUNCTION
  /* ================================================================
   * 함수명  : F_GET_TIME_TERM_HHMISS
   * 작성자  : 지성솔루션컨설팅
   * 작성일  : 2026-07-02
   * 수정이력:
   *   2026-07-02 - 지성솔루션컨설팅 - 최초 작성
   *   2026-07-02 - AI(Codex)       - 한글 주석 자동 추가
   * ================================================================
   * [AI 분석] 기능 설명:
   *   입력 파라미터와 기준 테이블을 이용해 업무 코드, 명칭, 수량, 상태 등의 조회 값을 반환한다.
   *   조회 실패 또는 예외 상황에서는 원본 로직에 정의된 기본값/NULL/오류 처리를 따른다.
   * ================================================================
   * [AI 분석] 파라미터:
   *   PARAM1  (IN, DATE) - 함수 입력값
   *   PARAM2  (IN, DATE) - 함수 입력값
   * ================================================================
   * [AI 분석] 반환값:
   *   VARCHAR2 - 함수 처리 결과값
   * ================================================================
   * [AI 분석] 참조 테이블:
   *   없음 (순수 연산 함수 또는 원본 소스 기준 명시 테이블 없음)
   * ================================================================
   * [AI 분석] 예외 처리:
   *   WHEN 절 또는 원본 예외 로직 기준으로 기본값 반환/애플리케이션 오류 처리를 수행한다.
   * ================================================================
   * [AI 분석] 복잡도:
   *   조건 분기: IF 2회 / 반복문: 0회
   *   DML: 없음
   * ================================================================
   * 사용 예시:
   *   SELECT F_GET_TIME_TERM_HHMISS(...) FROM DUAL;
   * ================================================================ */
 "F_GET_TIME_TERM_HHMISS"
  ( param1 IN DATE ,param2 IN DATE )
  RETURN  VARCHAR2 IS


lvl_time    NUMBER ;
lvl_time_m  NUMBER ;
lvl_str     VARCHAR2(10);

BEGIN



    lvl_time   := param2 - param1;
    lvl_time_m := ROUND((param2 - param1 ) * 24 * 60);

    IF ( lvl_time_m/60 < 999 AND lvl_time_m >= 0 ) THEN   
         lvl_str := trim(to_char(trunc(lvl_time_m/60),'900'))||':'||to_char( trunc(sysdate) + lvl_time, 'MI:SS');
    ELSE
         lvl_str := '###:##';
    END IF;

    RETURN lvl_str ;



EXCEPTION
   WHEN others THEN
       raise_application_error( -20003 , SQLERRM ) ;
END;
